/**
 * Get surnames by pinyin initial
 */
export function getBaijiaxingByInitial(): Record<string, BaijiaxingEntry[]> {
  const grouped: Record<string, BaijiaxingEntry[]> = {};

  BAIJIAXING_ENTRIES.forEach((entry) => {
    const initial = entry.pinyin.charAt(0).toUpperCase();
    if (!grouped[initial]) {
      grouped[initial] = [];
    }
    grouped[initial].push(entry);
  });

  return grouped;
}

/**
 * Get single character surnames only
 */
export function getSingleSurnames(): BaijiaxingEntry[] {
  return BAIJIAXING_ENTRIES.filter((e) => !e.isCompound);
}

/**
 * Get compound surnames only
 */
export function getCompoundSurnames(): BaijiaxingEntry[] {
  return BAIJIAXING_ENTRIES.filter((e) => e.isCompound);
}

/**
 * Find surname by character
 */
export function findBaijiaxingSurname(
  surname: string,
): BaijiaxingEntry | undefined {
  return BAIJIAXING_ENTRIES.find((e) => e.surname === surname);
}
