/**
 * Group surnames by pinyin initial letter
 */
export function getSurnamesByInitial(): Record<string, SurnameInfo[]> {
  const grouped: Record<string, SurnameInfo[]> = {};

  CHINESE_SURNAMES.forEach((surname) => {
    const initial = surname.pinyin.charAt(0).toUpperCase();
    if (!grouped[initial]) {
      grouped[initial] = [];
    }
    grouped[initial].push(surname);
  });

  return grouped;
}

/**
 * Get surname by character
 */
export function getSurnameInfo(surname: string): SurnameInfo | undefined {
  return CHINESE_SURNAMES.find((s) => s.surname === surname);
}

